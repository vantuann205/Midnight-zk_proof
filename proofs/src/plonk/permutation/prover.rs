#[cfg_attr(feature = "bench-internal", derive(Clone))]
#[derive(Debug)]
pub(crate) struct CommittedSet<F: PrimeField> {
    pub(crate) permutation_product_poly: Polynomial<F, Coeff>,
}

#[cfg_attr(feature = "bench-internal", derive(Clone))]
#[derive(Debug)]
pub(crate) struct Committed<F: PrimeField> {
    pub(crate) sets: Vec<CommittedSet<F>>,
}

/// Intermediate result from the compute phase of the permutation argument.
/// Holds commitments and Lagrange-form z polynomials, ready to be written to
/// the transcript and converted to coefficient form.
pub(crate) struct Computed<F: PrimeField, C> {
    pub(crate) commitments: Vec<C>,
    z_polys: Vec<Polynomial<F, LagrangeCoeff>>,
}

impl<F: WithSmallOrderMulGroup<3>, C> Computed<F, C> {
    /// Write commitments to the transcript in order, then convert z polynomials
    /// to coefficient form.
    pub(crate) fn write_and_convert<T: Transcript>(
        self,
        domain: &EvaluationDomain<F>,
        transcript: &mut T,
    ) -> Result<Committed<F>, Error>
    where
        C: Hashable<T::Hash>,
    {
        for commitment in &self.commitments {
            transcript.write(commitment)?;
        }

        let sets: Vec<_> = self
            .z_polys
            .into_par_iter()
            .map(|poly| CommittedSet {
                permutation_product_poly: domain.lagrange_to_coeff(poly),
            })
            .collect();

        Ok(Committed { sets })
    }
}

pub(crate) struct Evaluated<F: PrimeField> {
    constructed: Committed<F>,
    pub(crate) evaluated: Vec<permutation::Evaluated<F>>,
}

impl Argument {
    /// Compute the permutation argument: z polynomials and their commitments.
    /// Does not touch the transcript or rng — blinding values must be
    /// pre-generated by the caller and passed to the function.
    #[allow(clippy::too_many_arguments)]
    pub(crate) fn compute<F: WithSmallOrderMulGroup<3>, CS: PolynomialCommitmentScheme<F>>(
        &self,
        params: &CS::Parameters,
        pk: &plonk::ProvingKey<F, CS>,
        pkey: &ProvingKey<F>,
        advice: &[Polynomial<F, LagrangeCoeff>],
        fixed: &[Polynomial<F, LagrangeCoeff>],
        instance: &[Polynomial<F, LagrangeCoeff>],
        beta: F,
        gamma: F,
        all_blindings: Vec<Vec<F>>,
    ) -> Computed<F, CS::Commitment> {
        let domain = &pk.vk.domain;
        let n = domain.n as usize;
        let omega = domain.get_omega();

        // How many columns can be included in a single permutation polynomial?
        // We need to multiply by z(X) and (1 - (l_last(X) + l_blind(X))). This
        // will never underflow because of the requirement of at least a degree
        // 3 circuit for the permutation argument.
        assert!(pk.vk.cs_degree >= 3);
        let chunk_len = pk.vk.cs_degree - 2;
        let blinding_factors = pk.vk.cs.blinding_factors();

        let col_values = |column: &plonk::Column<Any>| match column.column_type() {
            Any::Advice(_) => advice,
            Any::Fixed => fixed,
            Any::Instance => instance,
        };

        let column_chunks: Vec<_> = self
            .columns
            .chunks(chunk_len)
            .zip(pkey.permutations.chunks(chunk_len))
            .collect();
        let num_sets = column_chunks.len();

        assert_eq!(all_blindings.len(), num_sets);
        for blindings in &all_blindings {
            assert_eq!(blindings.len(), blinding_factors);
        }

        // --- Phase 1: Compute modified_values for all sets in parallel. ---
        // Each set computes the product of fractions
        //   (p_j(ω^i) + δ^j ω^i β + γ) / (p_j(ω^i) + β s_j(ω^i) + γ)
        // The delta power for the first column of set s is δ^(s * chunk_len).
        let all_modified: Vec<Vec<F>> = column_chunks
            .par_iter()
            .enumerate()
            .map(|(set_idx, (columns, permutations))| {
                let mut modified_values = vec![F::ONE; n];

                // Denominator: multiply all columns' contributions.
                for (&column, permuted_column_values) in columns.iter().zip(permutations.iter()) {
                    let values = col_values(&column);
                    parallelize(&mut modified_values, |mv, start| {
                        for ((mv, value), pv) in mv
                            .iter_mut()
                            .zip(values[column.index()][start..].iter())
                            .zip(permuted_column_values[start..].iter())
                        {
                            *mv *= &(beta * pv + &gamma + value);
                        }
                    });
                }

                // Invert to obtain the denominator.
                modified_values.batch_invert();

                // Numerator: multiply all columns' contributions.
                // delta power for first column of this set.
                let mut deltaomega = F::DELTA.pow_vartime([(set_idx * chunk_len) as u64, 0, 0, 0]);
                for &column in columns.iter() {
                    let values = col_values(&column);
                    parallelize(&mut modified_values, |mv, start| {
                        let mut dw = deltaomega * &omega.pow_vartime([start as u64, 0, 0, 0]);
                        for (mv, value) in mv.iter_mut().zip(values[column.index()][start..].iter())
                        {
                            *mv *= &(dw * &beta + &gamma + value);
                            dw *= &omega;
                        }
                    });
                    deltaomega *= &F::DELTA;
                }

                modified_values
            })
            .collect();

        // --- Phase 2: Parallel prefix product across sets. ---
        //
        // Each set's z polynomial is a prefix product of its modified_values,
        // chained: set s+1 starts from set s's last non-blinded z value.
        //
        // Step A: Compute z for each set assuming last_z = 1. Record each
        //         set's uncorrected last_z value.
        // Step B: Sequential prefix product over the S uncorrected values
        //         to get per-set correction factors.
        // Step C: Multiply each set's z by its correction factor, apply
        //         blinding, and commit.

        // Step A: Parallel z computation (each set assumes last_z = 1).
        let z_and_uncorrected: Vec<(Polynomial<F, LagrangeCoeff>, F)> = all_modified
            .into_par_iter()
            .zip(all_blindings.par_iter())
            .map(|(modified_values, blindings)| {
                // Prefix product starting from 1.
                let mut z = Vec::with_capacity(n);
                z.push(F::ONE);
                for row in 1..n {
                    let tmp = z[row - 1] * &modified_values[row - 1];
                    z.push(tmp);
                }
                let mut z = domain.lagrange_from_vec(z);

                // Apply blinding factors.
                for (z, blind) in z[n - blinding_factors..].iter_mut().zip(blindings.iter()) {
                    *z = *blind;
                }

                // Record uncorrected last_z (before the blinding region).
                let uncorrected_last_z = z[n - (blinding_factors + 1)];

                (z, uncorrected_last_z)
            })
            .collect();

        // Step B: Sequential prefix product over the S uncorrected_last_z values.
        // This step accumulates the partial corrections to compute the actual
        // corrections needed for each chunk.
        // correction[0] = 1 (first set starts at 1).
        // correction[s] = ∏_{k<s} uncorrected_last_z[k].
        let mut corrections = Vec::with_capacity(num_sets);
        corrections.push(F::ONE);
        for s in 1..num_sets {
            let prev = corrections[s - 1] * &z_and_uncorrected[s - 1].1;
            corrections.push(prev);
        }

        // Step C: Apply corrections and commit.
        let committed: Vec<(CS::Commitment, Polynomial<F, LagrangeCoeff>)> = z_and_uncorrected
            .into_par_iter()
            .zip(corrections.par_iter())
            .map(|((mut z, _), &correction)| {
                // Multiply every z value by the correction factor.
                if correction != F::ONE {
                    parallelize(&mut z, |z, _| {
                        for elem in z.iter_mut() {
                            *elem *= &correction;
                        }
                    });
                }

                let commitment = CS::commit(params, &z);
                (commitment, z)
            })
            .collect();

        let (commitments, z_polys) = committed.into_iter().unzip();
        Computed {
            commitments,
            z_polys,
        }
    }
}

impl<F: PrimeField> super::ProvingKey<F> {
    pub(crate) fn open(&self, x: F) -> impl Iterator<Item = ProverQuery<'_, F>> + Clone {
        self.polys.iter().map(move |poly| ProverQuery { point: x, poly })
    }

    pub(crate) fn evaluate<T: Transcript>(
        &self,
        x: F,
        transcript: &mut T,
    ) -> Result<CommonEvaluated<F>, Error>
    where
        F: Hashable<T::Hash>,
    {
        // Compute all permutation polynomial evaluations up front.
        let permutation_evals: Vec<F> =
            self.polys.par_iter().map(|poly| eval_polynomial_seq(poly, x)).collect();

        // Write to transcript in order.
        for eval in &permutation_evals {
            transcript.write(eval)?;
        }

        Ok(CommonEvaluated { permutation_evals })
    }
}

impl<F: WithSmallOrderMulGroup<3>> Committed<F> {
    pub(crate) fn evaluate<T: Transcript, CS: PolynomialCommitmentScheme<F>>(
        self,
        pk: &plonk::ProvingKey<F, CS>,
        x: F,
        transcript: &mut T,
    ) -> Result<Evaluated<F>, Error>
    where
        F: Hashable<T::Hash>,
    {
        let domain = &pk.vk.domain;
        let blinding_factors = pk.vk.cs.blinding_factors();
        let x_next = domain.rotate_omega(x, Rotation::next());
        let x_last = domain.rotate_omega(x, Rotation(-((blinding_factors + 1) as i32)));
        let num_sets = self.sets.len();

        // Compute all evaluations in parallel: for each set, evaluate the
        // permutation product polynomial at x, omega*x, and (for all but the
        // last set) at the "last" rotation.
        let evaluated: Vec<permutation::Evaluated<F>> = self
            .sets
            .par_iter()
            .enumerate()
            .map(|(i, set)| permutation::Evaluated {
                permutation_product_eval: eval_polynomial(&set.permutation_product_poly, x),
                permutation_product_next_eval: eval_polynomial(
                    &set.permutation_product_poly,
                    x_next,
                ),
                permutation_product_last_eval: if i < num_sets - 1 {
                    Some(eval_polynomial(&set.permutation_product_poly, x_last))
                } else {
                    None
                },
            })
            .collect();

        // Write evaluations to transcript in the canonical order.
        for e in &evaluated {
            transcript.write(&e.permutation_product_eval)?;
            transcript.write(&e.permutation_product_next_eval)?;
            if let Some(last) = &e.permutation_product_last_eval {
                transcript.write(last)?;
            }
        }

        Ok(Evaluated {
            constructed: self,
            evaluated,
        })
    }
}

impl<F: WithSmallOrderMulGroup<3>> Evaluated<F> {
    pub(crate) fn open<'a, CS: PolynomialCommitmentScheme<F>>(
        &'a self,
        pk: &'a plonk::ProvingKey<F, CS>,
        x: F,
    ) -> impl Iterator<Item = ProverQuery<'a, F>> + Clone {
        let blinding_factors = pk.vk.cs.blinding_factors();
        let x_next = pk.vk.domain.rotate_omega(x, Rotation::next());
        let x_last = pk.vk.domain.rotate_omega(x, Rotation(-((blinding_factors + 1) as i32)));

        iter::empty()
            .chain(self.constructed.sets.iter().flat_map(move |set| {
                iter::empty()
                    // Open permutation product commitments at x and \omega x
                    .chain(Some(ProverQuery {
                        point: x,
                        poly: &set.permutation_product_poly,
                    }))
                    .chain(Some(ProverQuery {
                        point: x_next,
                        poly: &set.permutation_product_poly,
                    }))
            }))
            // Open it at \omega^{last} x for all but the last set. This rotation is only
            // sensical for the first row, but we only use this rotation in a constraint
            // that is gated on l_0.
            .chain(
                self.constructed.sets.iter().rev().skip(1).flat_map(move |set| {
                    Some(ProverQuery {
                        point: x_last,
                        poly: &set.permutation_product_poly,
                    })
                }),
            )
    }
}
